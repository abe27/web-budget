-- dbo.vmprod source

CREATE VIEW dbo.vmprod
AS
SELECT DISTINCT Formula.dbo.PROD.FCSKID, Formula.dbo.PROD.FCCODE, Formula.dbo.PROD.FCSNAME, Formula.dbo.PROD.FCNAME, Formula.dbo.PROD.FNSTDCOST AS FNPRICE, '' AS SUP_CODE,
                             (SELECT        MAX(FDDATE) AS Expr1
                               FROM            Formula.dbo.REFPROD
                               WHERE        (FCPROD = dbo.VMCORP.FCSKID)
                               GROUP BY FCPROD) AS DDATE
FROM            Formula.dbo.PROD INNER JOIN
                         dbo.VMCORP ON dbo.VMCORP.FCSKID = Formula.dbo.PROD.FCCORP
WHERE        (Formula.dbo.PROD.FCTYPE IN ('1', '2', '3', '4', '5', '6', '8', '9'));


