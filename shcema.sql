                         dbo.VMPROD_PRICE.FCCODE, dbo.VMPROD_PRICE.UNIT, dbo.vm_dept.FCNAME AS DEPTNAME, Formula.dbo.ORDERH.FNAMT, Formula.dbo.ORDERH.FNVATAMT, 
                         Formula.dbo.ORDERH.FNAMT + Formula.dbo.ORDERH.FNVATAMT AS TOTALAMT
FROM            Formula.dbo.ORDERH INNER JOIN
                         Formula.dbo.ORDERI ON Formula.dbo.ORDERI.FCORDERH = Formula.dbo.ORDERH.FCSKID INNER JOIN
                         dbo.VMPROD_PRICE ON dbo.VMPROD_PRICE.FCSKID = Formula.dbo.ORDERI.FCPROD INNER JOIN
                         dbo.vm_dept ON dbo.vm_dept.FCSKIDSECT = Formula.dbo.ORDERH.FCSECT
WHERE        (Formula.dbo.ORDERH.FCREFTYPE = 'PR');


-- dbo.VMPROD_PRICE source

CREATE VIEW dbo.VMPROD_PRICE
AS
SELECT        Formula.dbo.PROD.FCSKID, Formula.dbo.PROD.FCCODE, Formula.dbo.PROD.FCSNAME, Formula.dbo.PROD.FCNAME, ISNULL(Formula.dbo.PROD.FNSTDCOST, '1') AS FNPRICE, Formula.dbo.PROD.FCTYPE, 
                         SUBSTRING(SUBSTRING(Formula.dbo.PRODX4.FMMEMDATA, 6, LEN(Formula.dbo.PRODX4.FMMEMDATA)), - 8, LEN(Formula.dbo.PRODX4.FMMEMDATA)) + ' PCS/ ' + SUBSTRING(SUBSTRING(Formula.dbo.PRODX4.FMMEMDATA2,
                          5, LEN(Formula.dbo.PRODX4.FMMEMDATA2)), - 7, LEN(Formula.dbo.PRODX4.FMMEMDATA2)) AS packcode, Formula.dbo.UM.FCNAME AS UNIT, Formula.dbo.UM.FCSKID AS FCUM
FROM            Formula.dbo.PROD INNER JOIN
                         Formula.dbo.UM ON Formula.dbo.UM.FCSKID = Formula.dbo.PROD.FCUM LEFT OUTER JOIN
                         Formula.dbo.PRODX4 ON Formula.dbo.PRODX4.FCPROD = Formula.dbo.PROD.FCSKID INNER JOIN
                         dbo.VMCORP ON dbo.VMCORP.FCSKID = Formula.dbo.PROD.FCCORP
GROUP BY Formula.dbo.PROD.FCSKID, Formula.dbo.PROD.FCCODE, Formula.dbo.PROD.FCSNAME, Formula.dbo.PROD.FCNAME, Formula.dbo.PROD.FCTYPE, Formula.dbo.PRODX4.FMMEMDATA2, Formula.dbo.PROD.FNSTDCOST, 
                         Formula.dbo.PRODX4.FMMEMDATA, Formula.dbo.UM.FCNAME, Formula.dbo.UM.FCSKID;


